-- ====================================================================
-- CREACIÓN DE TABLAS
-- ====================================================================

CREATE TABLE clientes (
    id               INTEGER
                        GENERATED BY DEFAULT ON NULL AS IDENTITY
                        PRIMARY KEY,
    dni              VARCHAR2(15) UNIQUE NOT NULL,
    nombre_completo  VARCHAR2(100) NOT NULL,
    email            VARCHAR2(100) UNIQUE NOT NULL,
    telefono         VARCHAR2(20),
    provincia        VARCHAR2(50) NOT NULL,
    fecha_registro   DATE NOT NULL,
    puntos_fidelidad INTEGER DEFAULT 0
);

CREATE TABLE categorias (
    id     CHAR(3) PRIMARY KEY,   -- Siempre 3 caracteres, como en 'MOT', 'FRE', 'ACC'
    nombre VARCHAR2(50) NOT NULL
);

CREATE TABLE productos (
    id             INTEGER
                        GENERATED BY DEFAULT ON NULL AS IDENTITY
                        PRIMARY KEY,
    SKU            VARCHAR2(20) UNIQUE NOT NULL, -- Stock Keeping Unit (Unidad de Mantenimiento de Existencias)
    nombre         VARCHAR2(150) NOT NULL,
    categoria_id   CHAR(3) NOT NULL,
    marca          VARCHAR2(50) NOT NULL,
    precio_actual  NUMBER(8, 2) NOT NULL,
    stock          INTEGER NOT NULL,
    compatibilidad json,                        -- array de SKUs de compatibilidades entre productos

    CONSTRAINT fk_categoria FOREIGN KEY ( categoria_id )
        REFERENCES categorias ( id )
);

CREATE TABLE pedidos (
    id           INTEGER
                    GENERATED BY DEFAULT ON NULL AS IDENTITY
                    PRIMARY KEY,
    cliente_id   INTEGER NOT NULL,
    fecha_pedido DATE NOT NULL,
    estado       VARCHAR2(20) NOT NULL,
    metodo_pago  VARCHAR2(30),
    CONSTRAINT fk_cliente FOREIGN KEY ( cliente_id )
        REFERENCES clientes ( id ),
    CONSTRAINT chk_estado_pedido
        CHECK ( estado IN ( 'PENDIENTE', 'PAGADO', 'ENVIADO', 'ENTREGADO', 'CANCELADO' ) )
    -- Restricción para asegurar que solo entren estos valores exactos en mayúsculas
);

CREATE TABLE lineas_pedido (
    pedido_id       INTEGER NOT NULL,
    producto_id     INTEGER NOT NULL,
    cantidad        INTEGER NOT NULL,
    precio_unitario NUMBER(8, 2) NOT NULL,
    -- COLUMNA VIRTUAL: Se autocalcula sobre la marcha, pero no existe
    subtotal        NUMBER(8, 2) GENERATED ALWAYS AS ( cantidad * precio_unitario ) VIRTUAL,
    
    CONSTRAINT pk_lineas PRIMARY KEY ( pedido_id, producto_id ),
    CONSTRAINT fk_pedido FOREIGN KEY ( pedido_id )
        REFERENCES pedidos ( id )
            ON DELETE CASCADE,
    CONSTRAINT fk_producto FOREIGN KEY ( producto_id )
        REFERENCES productos ( id )
);


